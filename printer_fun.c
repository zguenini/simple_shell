#include "shell.h"
/**
 * Function to print an unsigned integer using putchar and run.
 *
 * @param n Unsigned Integer
 * @return Void
 */
void print_number(unsigned int n)
{
	unsigned int x = n;

	if ((x / 10) > 0)
		print_number(x / 10);

	_putchar(x % 10 + '0');
}
/**
 * Function to print a number using putchar and run.
 *
 * @param n Integer
 * @return Void
 */
void print_number_in(int n)
{
	unsigned int x = n;

	if (n < 0)
	{
		_putchar('-');
		x = -x;
	}
	if ((x / 10) > 0)
		print_number(x / 10);

	_putchar(x % 10 + '0');
}
